import type { BaseQuestion, ChatQuestion, ChoiceQuestion, Difficulty } from "./questions";

type Item = readonly [string, string, Difficulty, string];
type ChoiceItem = readonly [string, string, readonly string[], Difficulty, string, string];
type ChatItem = readonly [string, string, string, string, Difficulty, string, string];

function hint(answer: string) {
  return [`第一个字母是 ${answer[0]}。`, `可以先拆成常见拼音片段：${answer}。`];
}

function makeQuestion(prefix: string, type: "char" | "word", item: Item, index: number): BaseQuestion {
  const [chinese, answer, difficulty, lessonId] = item;
  return {
    id: `${prefix}-${String(index + 1).padStart(3, "0")}`,
    type,
    chinese,
    answer,
    difficulty,
    lessonId,
    hints: hint(answer),
    explanation: `“${chinese}”使用拼音输入时输入 ${answer}。`
  };
}

function makeChoice(item: ChoiceItem, index: number): ChoiceQuestion {
  const [chinese, answer, options, difficulty, lessonId, explanation] = item;
  return {
    id: `choice-extra-${String(index + 1).padStart(3, "0")}`,
    type: "choice",
    chinese,
    answer,
    options: [...options],
    difficulty,
    lessonId,
    hints: hint(answer),
    explanation
  };
}

function makeChat(prefix: string, item: ChatItem, index: number): ChatQuestion {
  const [sceneName, scene, chinese, answer, difficulty, lessonId, explanation] = item;
  return {
    id: `${prefix}-${String(index + 1).padStart(3, "0")}`,
    type: "chat",
    chinese,
    answer,
    difficulty,
    lessonId,
    speaker: sceneName.includes("温老师") ? "温老师" : sceneName.includes("客户") ? "客户" : "朋友",
    scene,
    prompt: `请用拼音输入“${chinese}”。`,
    hints: hint(answer),
    explanation
  };
}

